// An array is a special variable, which can hold more than one value:
// const cars = ["Saab", "Volvo", "BMW"];

// Why Use Arrays?
// If you have a list of items (a list of car names, for example), 
// storing the cars in single variables could look like this:

// let car1 = "Saab";
// let car2 = "Volvo";
// let car3 = "BMW";

// However, what if you want to loop through the cars and find a specific one?
//  And what if you had not 3 cars, but 300?

// The solution is an array!

// An array can hold many values under a single name, and you can access the values 
// by referring to an index number.

// What are arrays in JavaScript?
// Arrays are used to store multiple values in a single variable. This
//  is compared to a variable that can store only one value. Each item 
//  in an array has a number attached to it, called a numeric index, that
//   allows you to access it. In JavaScript, arrays start at index zero 
//   and can be manipulated with various methods.
